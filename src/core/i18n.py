def get_language(settings_language: str | None) -> str:
    language = (settings_language or "en").lower()
    return language if language in SUPPORTED_LANGUAGES else "en"


def tr(key: str, language: str | None) -> str:
    lang = get_language(language)
    return TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS["en"].get(key) or key


def is_rtl(language: str | None) -> bool:
    return get_language(language) in RTL_LANGUAGES
